## Las bases del Pilates: respiración y postura

El Pilates rara vez empieza con un movimiento espectacular. Empieza con una presencia más atenta: sentir la respiración, observar la postura y comprender cómo se organiza el cuerpo antes de moverse. Estas bases pueden parecer sencillas, pero transforman la práctica. Ayudan a trabajar con más precisión, a proteger las articulaciones y a crear una sensación de estabilidad que acompaña cada ejercicio.

## La respiración como motor del movimiento

En Pilates, la respiración no es solo un ritmo agradable. Ayuda a activar el centro, a soltar tensiones innecesarias y a dar dirección al movimiento. A menudo buscamos una respiración torácica lateral: al inhalar, las costillas se abren hacia los lados y hacia la espalda, como un acordeón. Al exhalar, las costillas se cierran suavemente y el abdomen vuelve hacia la columna sin endurecerse.

Esta forma de respirar permite mantener espacio en la parte alta del cuerpo mientras se activan los músculos profundos. También evita bloquear el aire durante el esfuerzo, algo frecuente cuando nos concentramos demasiado en hacerlo "perfecto". Una regla sencilla para empezar: inhala para preparar, exhala para acompañar la acción. La exhalación se convierte entonces en un apoyo, como un hilo que guía el movimiento.

## Una postura estable, no rígida

La postura en Pilates no es una posición militar. Es viva, adaptable y cómoda. Buscamos alinear los principales puntos del cuerpo: cabeza sobre la caja torácica, caja torácica sobre la pelvis, pelvis estable y pies bien apoyados cuando estamos de pie. En la colchoneta ocurre lo mismo: la columna se alarga, los hombros descansan y el cuello queda libre.

Lo importante es no confundir alineación con rigidez. Una postura correcta debe permitir respirar. Si necesitas apretar la mandíbula, subir los hombros o contraer el abdomen con fuerza para sostener una posición, probablemente la intensidad es excesiva o el ajuste no es el adecuado. El Pilates invita a encontrar un compromiso profundo con la menor tensión posible.

## El centro como punto de apoyo

Se habla mucho del "centro" o "powerhouse". Es la zona profunda que integra abdominales, suelo pélvico, músculos de la espalda y respiración. El objetivo no es meter el abdomen al máximo, sino crear un apoyo interno. Imagina una cremallera suave que sube desde la parte baja del abdomen hacia el esternón durante la exhalación.

Este apoyo hace que los movimientos sean más fluidos. Ayuda a levantar una pierna sin arquear la zona lumbar, a girar el torso sin tirar de los hombros y a alargar los brazos sin perder equilibrio. Cuanto más disponible está el centro, menos compensaciones necesita el cuerpo.

## Cómo practicarlo en el día a día

Antes de cada sesión, dedica un minuto a volver a lo esencial. Túmbate o siéntate cómodamente. Inhala sintiendo cómo las costillas se expanden. Exhala despacio dejando que el centro se active. Comprueba que los hombros siguen bajos, que el rostro está relajado y que la columna mantiene una sensación de longitud.

Después, lleva esa calidad a los ejercicios más simples: basculaciones de pelvis, aperturas de brazos, movilidad de columna o elevación de una pierna. La progresión no aparece solo con movimientos más difíciles, sino con una escucha más precisa en los movimientos básicos.

## Una base sólida para avanzar

Respiración y postura son dos puertas de entrada al Pilates. Hacen que la práctica sea más segura, elegante y eficaz. Al volver con frecuencia a estos fundamentos, construyes una relación más inteligente con tu cuerpo. Cada sesión se convierte en una oportunidad para fortalecer, pero también para aprender a moverte con calma, precisión y confianza.
